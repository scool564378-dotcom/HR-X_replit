export type Answer = "yes" | "partial" | "no" | null;

export function calculateScore(answers: Answer[]): number {
  let total = 0;
  for (const a of answers) {
    if (a === "yes") total += 10;
    else if (a === "partial") total += 5;
  }
  return total;
}

export function getResultLevel(score: number): { title: string; text: string; color: string } {
  if (score >= 80) {
    return {
      title: "Хорошая база для удалённой работы",
      text: "У вас уже есть хорошая основа для удалённой работы. Можно смело переходить к следующему шагу и смотреть подходящие вакансии.",
      color: "emerald",
    };
  }
  if (score >= 50) {
    return {
      title: "Есть что немного подтянуть",
      text: "У вас уже есть база. Несколько моментов стоит подтянуть, и искать удалённую работу будет проще и спокойнее.",
      color: "amber",
    };
  }
  return {
    title: "Нужна небольшая подготовка",
    text: "Сейчас есть несколько важных моментов, которые лучше заранее улучшить. Это нормально: большинство из них можно подтянуть без сложностей.",
    color: "red",
  };
}
